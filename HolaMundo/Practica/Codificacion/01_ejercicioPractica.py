# 01 Ejercicio promedio de dos numeros

# entrada
num1 = int(input("Ingresa un valor: "))
num2 = int(input("Ingresa un segundo valor: "))

# proceso
s = num1 + num2
p = s/2

# salida
print("-------------------------------------")
print("Ejercicio 1: Promedio de dos numeros")
print("-------------------------------------")
print("Ingrese notas:")
print(f"N1: {num1}")
print(f"N2: {num2}")
print("\nSalida:")
print("-------------------------------------")
print("Promedio: {}".format(p))
