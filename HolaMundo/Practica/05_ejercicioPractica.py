# 05 Ejercicio radio de un circulo

# Inicio
pi = float(3.1416)
radio = float((input("Digite el radio del circulo: ")))
# Proceso
v = 4/3 * pi * radio ** 3
# Salida
print("-------------------------------------")
print("Ejercicio 5: Circulo")
print("-------------------------------------")
print("Dato ingresado:")
print(f"Radio: {radio}")
print("\nSalida:")
print("-------------------------------------")
print("Volumen de la esfera: {}".format(v))
