# 0.2 Se ingresan las notas de dos evaluaciones de un alumno. Determinar la nota promedio

nota_evaluacion1 = float(input("Ingresa la primer nota: "))
nota_evaluacion2 = float(input("Ingresa la segunda nota: "))

suma_notas = (nota_evaluacion1 + nota_evaluacion2) / 2

print(f"La nota promedio: {suma_notas}")
