# 27 Dada una cadena que consta de palabras y espacios, devuelve la longitud de la última palabra de la cadena.
# Una palabra es un máximo subcadena que consta únicamente de caracteres que no son espacios.

palabra = "Argentina, Mexico, Uruguay, Canada"

print(f"Cantidad de letras: {len(palabra)}")
print(f"Palabra: {palabra}")
print(f"Ultima palabra: {palabra[-6:]}")
